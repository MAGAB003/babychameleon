CREATE TABLE Customer(
ID VARCHAR AUTO_INCREMENT PRIMARY KEY,
FirstName VARCHAR(50) NOT NULL,
LastName VARCHAR(50) NOT NULL,
Email VARCHAR(50) NOT NULL,
StreetAddress VARCHAR(50) NOT NULL,
PostalCode VARCHAR(50) NOT NULL,
City VARCHAR(50) NOT NULL,
CreditCardNumber INT,
CreditCardExpirationDate VARCHAR(50),
CvvCode INT,
IDCounter INT NOT NULL
);
CREATE TABLE Admin(
ID VARCHAR AUTO_INCREMENT PRIMARY KEY,
FirstName VARCHAR(50) NOT NULL,
LastName VARCHAR(50) NOT NULL,
Email VARCHAR(50) NOT NULL
);

CREATE TABLE Subscription(
ID VARCHAR AUTO_INCREMENT PRIMARY KEY,
Name VARCHAR(50) NOT NULL,
Description VARCHAR(50) NOT NULL,
ImgID VARCHAR(50) NOT NULL,
Age_Group VARCHAR(50) NOT NULL,
Theme VARCHAR(50) NOT NULL
);

CREATE TABLE BabyChameleonUser(
ID VARCHAR AUTO_INCREMENT PRIMARY KEY,
Password VARCHAR(50) NOT NULL,
CustomerID VARCHAR(50),
AdminID VARCHAR(50)
);

CREATE TABLE Child(
ID VARCHAR AUTO_INCREMENT PRIMARY KEY,
Name VARCHAR(50) NOT NULL,
AgeGroup VARCHAR(50) NOT NULL,
Birthday DATE NOT NULL,
FavoriteColor VARCHAR(50),
FavoriteGarment VARCHAR(50),
ParentID VARCHAR(50) NOT NULL,
SubscriptionID VARCHAR(50) NOT NULL
);


